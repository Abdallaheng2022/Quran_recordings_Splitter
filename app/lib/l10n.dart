/// حالة اللغة الحالية — الاستماع لها يعيد بناء التطبيق كله.
final ValueNotifier<String> appLocale = ValueNotifier('ar');

Future<void> loadSavedLocale() async {
  final p = await SharedPreferences.getInstance();
  final saved = p.getString('locale');
  if (saved != null && kLanguages.any((l) => l.code == saved)) {
    appLocale.value = saved;
  }
}

Future<void> setLocale(String code) async {
  appLocale.value = code;
  final p = await SharedPreferences.getInstance();
  await p.setString('locale', code);
}

bool get isRtl => kLanguages.firstWhere((l) => l.code == appLocale.value).rtl;

/// t('key') — الترجمة بالمفتاح للغة الحالية (يرجع الإنجليزية إن غابت).
String t(String key) {
  final lang = appLocale.value;
  return _strings[lang]?[key] ?? _strings['en']?[key] ?? key;
}

/// امزج هذا مع أي State لإعادة بنائها فور تغيّر اللغة (تحديث كل النصوص حيًّا).
mixin LocaleRebuild<T extends StatefulWidget> on State<T> {
  void _localeRebuild() {
    if (mounted) setState(() {});
  }

  @override
  void initState() {
    super.initState();
    appLocale.addListener(_localeRebuild);
  }

  @override
  void dispose() {
    appLocale.removeListener(_localeRebuild);
    super.dispose();
  }
}

